ngay = int(input("Give me a number (form 1 to 7): "))
if ngay == 1:
	print("Monday")
elif ngay == 2:
	print("Tuesday")
elif ngay == 3:
	print("Wednesday")
elif ngay == 4:
	print("Thursday")
elif ngay == 5:
	print("Friday")
elif ngay == 6:
	print("Saturday")
elif ngay == 7:
	print("Sunday")
else:
	print("Error, out of range")